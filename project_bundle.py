def restore(dest_dir: Path) -> None:
    data = base64.b64decode(_BUNDLE_B64)
    pos = 0
    count = 0
    while pos < len(data):
        nl = data.index(b"\n", pos)
        header1 = data[pos:nl]
        pos = nl + 1
        assert header1.startswith(FILE_MARKER), header1
        rel = header1[len(FILE_MARKER) + 1:].decode("utf-8")

        nl = data.index(b"\n", pos)
        header2 = data[pos:nl]
        pos = nl + 1
        assert header2.startswith(BYTES_MARKER), header2
        size = int(header2[len(BYTES_MARKER) + 1:])

        content = data[pos:pos + size]
        pos += size + 1  # skip the trailing newline separator

        target = dest_dir / rel
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_bytes(content)
        count += 1

    print(f"Restored {count} files into {dest_dir}")


def main() -> None:
    dest = Path(sys.argv[1]) if len(sys.argv) > 1 else Path(__file__).parent / "TrailDCA"
    restore(dest)


if __name__ == "__main__":
    main()
